#!/usr/bin/env python
import rospy
from geometry_msgs.msg import Twist
import time
from math import radians
class Assign2():
	def move(self,x):
		rospy.init_node('GoForward', anonymous=True)
		self.cmd_vel = rospy.Publisher('cmd_vel_mux/input/navi', Twist, queue_size=10)
		r = rospy.Rate(10)
		move_cmd = Twist()
		move_cmd.linear.x = 0.2
		move_cmd.angular.z = 0
		i=0
		while(i<x):
		   self.cmd_vel.publish(move_cmd)
		   r.sleep()
		   #curr=rospy.Time.now().to_sec()
		   i=i+0.1
		move_cmd.linear.x = 0
		move_cmd.angular.z = 0
		self.cmd_vel.publish(move_cmd)
	def rotate(self,x):
		rospy.init_node('GoForward', anonymous=True)
		self.cmd_vel = rospy.Publisher('cmd_vel_mux/input/navi', Twist, queue_size=10)
		move_cmd = Twist()
		r=rospy.Rate(10)
		move_cmd.linear.x = 0
		move_cmd.angular.z = radians(x)
		for i in range (0,10):
			self.cmd_vel.publish(move_cmd)
			r.sleep()
		move_cmd.linear.x = 0
		move_cmd.angular.z = 0
		self.cmd_vel.publish(move_cmd)
